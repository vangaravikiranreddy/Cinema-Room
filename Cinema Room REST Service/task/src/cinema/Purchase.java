package cinema;

import java.util.UUID;

public class Purchase {
    public UUID token;
    public Seat ticket;

    Purchase() {}

    Purchase(UUID token) {
        this.token = token;
    }

    public UUID getToken() {
        return token;
    }

    public void setToken(UUID token) {
        this.token = token;
    }

    public Seat getTicket() {
        return ticket;
    }

    public void setTicket(Seat ticket) {
        this.ticket = ticket;
    }
}
